let currentLang = localStorage.getItem('arlan_lang') || 'ru';

function t(key) {
  if (translations[currentLang]?.[key]) return translations[currentLang][key];
  if (window.priceTranslations?.[currentLang]?.[key]) return window.priceTranslations[currentLang][key];
  if (translations.ru?.[key]) return translations.ru[key];
  if (window.priceTranslations?.ru?.[key]) return window.priceTranslations.ru[key];
  return key;
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('arlan_lang', lang);
  applyTranslations();
  document.documentElement.lang = lang;
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = t(key);
    if (!val || val === key) return;

    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else if (el.tagName === 'OPTION') {
      // Translate individual option elements
      el.textContent = val;
    } else if (el.tagName === 'SELECT') {
      // Translate the default (placeholder) option if select itself has data-i18n
      if (el.options[0] && el.options[0].value === '') {
        el.options[0].textContent = val;
      }
    } else {
      el.textContent = val;
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    const val = t(key);
    if (val && val !== key) el.innerHTML = val;
  });
}

function initLang() {
  applyTranslations();
  document.documentElement.lang = currentLang;
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
    btn.addEventListener('click', () => setLang(btn.dataset.lang));
  });
}

window.t = t;
window.setLang = setLang;
window.initLang = initLang;
