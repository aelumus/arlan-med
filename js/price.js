function buildPriceTables() {
  const tabsEl = document.querySelector('.price-tabs');
  const contentEl = document.querySelector('.price-content');
  if (!tabsEl || !contentEl) return;

  Object.entries(priceData).forEach(([key, cat], i) => {
    // Tab button
    const tab = document.createElement('button');
    tab.className = 'price-tab' + (i === 0 ? ' active' : '');
    tab.dataset.tab = key;
    // Map existing label to its translation key instead of hardcoding
    tab.dataset.i18n = 'price_cat_' + key; 
    tab.textContent = cat.label;
    tabsEl.appendChild(tab);

    // Panel
    const panel = document.createElement('div');
    panel.className = 'price-panel' + (i === 0 ? ' active' : '');
    panel.dataset.panel = key;

    const table = document.createElement('table');
    table.className = 'price-table';
    table.innerHTML = `
      <thead>
        <tr><th data-i18n="th_service">Услуга</th><th data-i18n="th_price">Цена</th></tr>
      </thead>
      <tbody>
        ${cat.items.map(([name, price], j) => `
          <tr>
            <td data-i18n="pr_${key}_${j}">${name}</td>
            <td><span class="price-amount">${price}</span></td>
          </tr>
        `).join('')}
      </tbody>`;
    panel.appendChild(table);
    contentEl.appendChild(panel);
  });

  // Tab switching
  tabsEl.addEventListener('click', e => {
    const tab = e.target.closest('.price-tab');
    if (!tab) return;
    tabsEl.querySelectorAll('.price-tab').forEach(t => t.classList.remove('active'));
    contentEl.querySelectorAll('.price-panel').forEach(p => p.classList.remove('active'));
    tab.classList.add('active');
    contentEl.querySelector(`[data-panel="${tab.dataset.tab}"]`)?.classList.add('active');
    // Reset search
    const search = document.querySelector('.price-search');
    if (search) { search.value = ''; filterPrice(''); }
  });
}

function filterPrice(query) {
  const q = query.toLowerCase().trim();
  const activePanel = document.querySelector('.price-panel.active');
  if (!activePanel) return;

  const rows = activePanel.querySelectorAll('tbody tr');
  let found = 0;
  rows.forEach(row => {
    const text = row.cells[0]?.textContent.toLowerCase() || '';
    const match = !q || text.includes(q);
    row.style.display = match ? '' : 'none';
    if (match) found++;
  });

  // no results message
  let noRes = activePanel.querySelector('.no-results');
  if (!found) {
    if (!noRes) {
      noRes = document.createElement('tr');
      noRes.className = 'no-results';
      noRes.innerHTML = '<td colspan="2" style="text-align:center;padding:3rem;color:#6B7280" data-i18n="price_no_results">Ничего не найдено</td>';
      activePanel.querySelector('tbody').appendChild(noRes);
    }
    noRes.style.display = '';
  } else if (noRes) {
    noRes.style.display = 'none';
  }
}

document.addEventListener('DOMContentLoaded', () => {
  buildPriceTables();
  if (typeof applyTranslations === 'function') {
    applyTranslations();
  }
  const search = document.querySelector('.price-search');
  if (search) {
    search.addEventListener('input', e => filterPrice(e.target.value));
  }
});
