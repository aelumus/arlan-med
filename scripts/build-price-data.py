def js_string(s):
    return json.dumps(s, ensure_ascii=False)


def generate_price_js(data):
    lines = [
        '/* =============================================',
        '   price.js — Price list tabs + search',
        '   Arlan Med Dental Clinic',
        '   ============================================= */',
        '',
        'const priceData = {',
    ]
    for key, cat in data.items():
        lines.append(f"  {key}: {{")
        lines.append(f"    label: {js_string(cat['label'])},")
        lines.append('    items: [')
        for name, price in cat['items']:
            lines.append(f"      [{js_string(name)}, {js_string(price)}],")
        lines.append('    ]')
        lines.append('  },')
    lines.append('};')
    lines.append('')
    return '\n'.join(lines)


def generate_price_i18n(data):
    for lang in ('kz', 'en'):
        if lang not in PRICE_TRANSLATIONS:
            raise KeyError(f'Missing language translations: {lang}')
        for cat_key, cat in data.items():
            if cat_key not in PRICE_TRANSLATIONS[lang]:
                raise KeyError(f'Missing {lang} translations for category: {cat_key}')
            source_count = len(cat['items'])
            translated_count = len(PRICE_TRANSLATIONS[lang][cat_key])
            if translated_count != source_count:
                raise ValueError(
                    f'Translation count mismatch for {lang}/{cat_key}: '
                    f'{translated_count} != {source_count}'
                )

    total = sum(len(v['items']) for v in data.values())
    lines = [
        '/* =============================================',
        '   price-i18n.js — Price list item translations (RU / KZ / EN)',
        '   Arlan Med Dental Clinic',
        f'   {total} items',
        '   ============================================= */',
        '',
        'const priceTranslations = {',
    ]
    for lang in ('ru', 'kz', 'en'):
        lines.append(f'  {lang}: {{')
        for cat_key, cat in data.items():
            for i, (name, _price) in enumerate(cat['items']):
                if lang == 'ru':
                    translated_name = name
                else:
                    translated_name = PRICE_TRANSLATIONS[lang][cat_key][i]
                lines.append(f'    pr_{cat_key}_{i}: {js_string(translated_name)},')
        lines.append('  },')
    lines.append('};')
    lines.append('')
    lines.append('window.priceTranslations = priceTranslations;')
    lines.append('')
    return '\n'.join(lines)


